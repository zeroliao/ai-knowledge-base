param(
  [string]$KeyFile = "key.md",
  [string]$EnvFile = ".env.local"
)

$ErrorActionPreference = "Stop"

function Test-PortFree {
  param([int]$Port)
  $client = New-Object System.Net.Sockets.TcpClient
  try {
    $async = $client.BeginConnect("127.0.0.1", $Port, $null, $null)
    if ($async.AsyncWaitHandle.WaitOne(200)) {
      $client.EndConnect($async)
      return $false
    }
    return $true
  } catch {
    return $true
  } finally {
    $client.Close()
  }
}

function Get-FreePort {
  param([int]$Preferred)
  $port = $Preferred
  while (-not (Test-PortFree -Port $port)) {
    $port++
  }
  return $port
}

function Read-KeyConfig {
  param([string]$Path)
  if (-not (Test-Path -LiteralPath $Path)) {
    throw "Missing $Path. Put sub2api baseUrl and API key in this file."
  }

  $content = Get-Content -Raw -Encoding UTF8 -LiteralPath $Path
  $chatBaseUrl = $null
  $chatApiKey = $null
  $embeddingBaseUrl = $null
  $embeddingApiKey = $null
  $chatModel = "gpt-5.5"
  $embeddingModel = "text-embedding-3-large"
  $section = "chat"

  foreach ($line in ($content -split "`r?`n")) {
    $trimmed = $line.Trim()
    if (-not $trimmed) { continue }

    $urlMatch = [regex]::Match($trimmed, 'https?://[^\s''"<>]+')
    if ($urlMatch.Success) {
      if ($section -eq "embedding") {
        $embeddingBaseUrl = $urlMatch.Value.Trim().TrimEnd("/")
      } else {
        $chatBaseUrl = $urlMatch.Value.Trim().TrimEnd("/")
      }
      continue
    }

    if ($trimmed -match '^(?i)(apiKey|api_key|api\s*key|key)\s*[:я╝Ъ=]\s*(.+)$') {
      if ($section -eq "embedding") {
        $embeddingApiKey = $Matches[2].Trim()
      } else {
        $chatApiKey = $Matches[2].Trim()
      }
    } elseif ($trimmed -match '^(?i)(chatModel|chat_model|chat\s*model)\s*[:я╝Ъ=]\s*(.+)$') {
      $chatModel = $Matches[2].Trim()
    } elseif ($trimmed -match '^(?i)(embeddingModel|embedding_model|embedding\s*model)\s*[:я╝Ъ=]\s*(.+)$') {
      $embeddingModel = $Matches[2].Trim()
    } elseif ($trimmed -match '(?i)(embedding|хРСщЗП)') {
      $section = "embedding"
    } elseif ($trimmed -match '(?i)(chat|gpt|щАЪчФи)') {
      $section = "chat"
    }
  }

  if (-not $chatBaseUrl -or -not $chatApiKey) {
    throw "Could not parse chat baseUrl or API key from $Path."
  }

  if (-not $embeddingBaseUrl) {
    $embeddingBaseUrl = $chatBaseUrl
  }
  if (-not $embeddingApiKey) {
    $embeddingApiKey = $chatApiKey
  }

  return @{
    ChatBaseUrl = $chatBaseUrl.TrimEnd("/")
    ChatApiKey = $chatApiKey
    ChatModel = $chatModel
    EmbeddingBaseUrl = $embeddingBaseUrl.TrimEnd("/")
    EmbeddingApiKey = $embeddingApiKey
    EmbeddingModel = $embeddingModel
  }
}

$config = Read-KeyConfig -Path $KeyFile

